import time
import os
import sys

##########################TEXT AND ASCII ART DISPLAY FUNTIONS#######################
def typewriter(text, delay):
	for row in text:
		for char in row:
			sys.stdout.write(char)
			sys.stdout.flush()
			time.sleep(delay)
		print()
		time.sleep(1)
		os.system('clear')

def artdisplay(text, delay):
	for row in text:
		print(row)
		time.sleep(delay)

